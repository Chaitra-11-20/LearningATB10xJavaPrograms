package ex_12_DoWhile;

public class Lab122 {
    public static void main(String[] args) {
        int a = 0;
        do {
            System.out.println(a);
            a++;
        } while (a < 10);


        int b = 80;

        do {
            System.out.println(b);
            b--;
        }
        while (b >= 0);
    }
}
