package Tasks_Loops;

public class Reverse_Order {
    public static void main(String[] args) {

        for(int i=10;i>=1;i--)
        {
            System.out.println(i);
        }
        System.out.println("===============");

        int i=10;
        while( i>=1)
        {
            System.out.println(i);
            i--;
        }
    }
}
